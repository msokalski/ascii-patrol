struct CAMPAIGN_MODAL : MODAL
{
	// we can have one of 2 sub-modals active: gamelevel or interscreen
	MODAL* level_modal;
	MODAL* inter_modal;

	int iCourse;
	int iLevel;
	int iSubLev;

	SCREEN* s;
	int w;
	int h;

	int lives;
	int score;
	const COURSE* current_course;
	const LEVEL* current_level;

	int level_time;

	int startlives; // lives when entering level
	char hitbin[1024]; // so max level size is 8192!

	virtual ~CAMPAIGN_MODAL()
	{
		if (level_modal)
			delete level_modal;
		if (inter_modal)
			delete inter_modal;
	}

	CAMPAIGN_MODAL(SCREEN* scr, int ic, int il)
	{
		s=scr;
		w=scr->w;
		h=scr->h;

		iCourse = ic;
		iLevel  = il;
		iSubLev = 0;

		// iSubLev = 4;

		lives = 3;
		score = 0;
		current_course = campaign + iCourse;
		current_level = current_course->level + iLevel;

		startlives = lives;

		memset(hitbin,0,1024);
		level_time = 0;

		FILE* record_file = 0;
		fopen_s(&record_file,record_path(),"wb");
		if (record_file)
		{
			// write header
			unsigned char sign[8]="REC0";
			fwrite(sign,4,1,record_file);

			fwrite(&iCourse,4,1,record_file);
			fwrite(&iLevel,4,1,record_file);
			fwrite(&iSubLev,4,1,record_file);
			fwrite(&lives,4,1,record_file);

			// this part must be written before each (re)spawn!
			fwrite(&w,4,1,record_file);
			fwrite(&h,4,1,record_file);
			twister_write(record_file);

			crypt_ini();

			int n[4];
			memcpy(n,conf_player.name,16);
			n[0] ^= score;
			n[1] ^= score;
			n[2] ^= score;
			n[3] ^= score;
			crypt_enc(n+0);
			crypt_enc(n+1);
			crypt_enc(n+2);
			crypt_enc(n+3);
			fwrite(n,1,16,record_file);

			int a =conf_player.avatar;
			a ^= score;
			crypt_enc(&a);
			fwrite(&a,4,1,record_file);

			fclose(record_file);
			// ready for appending
		}

		level_modal = new LEVEL_MODAL(
			&global_screen,
			lives,startlives,
			&score,
			&level_time,
			conf_player.name,
			current_course->name,
			current_level,
			iSubLev, hitbin);

		inter_modal = 0;
	}

	virtual int Run()
	{
		// RETURN -2 is reserved for temporal exits to menu
		// RETURN -3 is permanent end of game exit.

		if (level_modal)
		{
			// in-game
			int ret = level_modal->Run();

			w=s->w;
			h=s->h;

			if (ret>=0)
			{
				// player just died in returned sublevel
				lives--;

				if (lives)
				{
					iSubLev = ret;

					delete level_modal;

					FILE* record_file = 0;
					fopen_s(&record_file,record_path(),"ab");
					if (record_file)
					{
						int t;
						
						t=w^score; 
						crypt_enc(&t);
						fwrite(&t,4,1,record_file);
						
						t=h^score; 
						crypt_enc(&t);
						fwrite(&t,4,1,record_file);
						
						twister_write(record_file);

						fclose(record_file);
					}

					level_modal = new LEVEL_MODAL(
						&global_screen,
						lives,startlives,
						&score,
						&level_time,
						conf_player.name,
						current_course->name,
						current_level,
						iSubLev, hitbin);
				}
				else
				{
					// game over
					delete level_modal;
					level_modal = 0;

					FILE* record_file = 0;
					fopen_s(&record_file,record_path(),"ab");
					if (record_file)
					{
						unsigned int chk[5] = 
						{
							twister_rand(),
							twister_rand(),
							crypt_chk(),
							twister_rand(),
							twister_rand()
						};

						fwrite(chk,1,15+(rand()%5),record_file);
						fclose(record_file);
					}

					// post recording to the server
					// delete recording file?
					post_hiscore();
					
					inter_modal = new GAMEOVER_MODAL(
						&global_screen,
						&score,
						current_level);
				}

				return 0;
			}

			// quit
			if (ret==-2)
			{
				// quit to menu, pause & show confirmation?
				// maybe not necessary as we are going to have continue thing!
				// ...

				// destroy game
				/*
				delete level_modal;
				level_modal = 0;
				*/

				return -2;
			}

			if (ret==-3)
			{
				// still playing
				return -1;
			}

			///////////////////////////
			// so ret is -1

			// level finished. 
			// prepare interscreen

			// calc hitacc from hitbin
			int hitacc = 0;
			for (int i=0; i<1024; i++)
			{
				unsigned char b = hitbin[i];
				while (b)
				{
					if (b&1)
						hitacc++;
					b >>= 1;
				}
			}

			// parse level data to find hitmax
			int hitmax = 0;
			for (int i=0; current_level->sprite[i]; i++)
			{
				char spr = current_level->sprite[i];
				switch (spr)
				{
					case 'U':
					case 'D':
					case 'B':
					case 'r':
					case 't':
					case 'c':
					case 'h':
					case 'b':
						hitmax++;
				}
			}

			delete level_modal;
			level_modal = 0;

			inter_modal = new INTER_MODAL(
				&global_screen,
				lives, startlives,
				&score,
				current_course->name,
				current_level,
				level_time,
				hitacc, hitmax);


			// increment level
			level_time = 0;
			hitacc = 0;
			startlives = lives;
			memset(hitbin,0,1024);

			iSubLev=0;
			current_level++;
			iLevel++;
			if (!current_level->height)
			{
				current_course++;
				iCourse++;

				// update progress in conf
				if (iCourse>conf_campaign.passed)
				{
					conf_campaign.passed = iCourse;
					SaveConf();
				}

				if (!current_course->level || (current_course->flags&0x1))
				{
					current_course = campaign + 0;
					iCourse=0;
					// RANDOM GENERATOR SURVIVAL MDOE ?
				}
				current_level = current_course->level + 0;
				iLevel=0;
			}

			return -1;
		}
		else
		if (inter_modal)
		{
			int ret = inter_modal->Run();

			w=s->w;
			h=s->h;

			if (!lives)
			{
				if (ret)
				{
					delete inter_modal;
					inter_modal = 0;
					// gameover->menu
					// either by esc or any key
					return -3;
				}
				return -1;
			}

			if (ret == -1)
			{
				// next level
				delete inter_modal;
				inter_modal = 0;

				FILE* record_file = 0;
				fopen_s(&record_file,record_path(),"ab");
				if (record_file)
				{

					int t;
					
					t=w^score; 
					crypt_enc(&t);
					fwrite(&t,4,1,record_file);
					
					t=h^score; 
					crypt_enc(&t);
					fwrite(&t,4,1,record_file);
					
					twister_write(record_file);

					fclose(record_file);
				}

				level_modal = new LEVEL_MODAL(
					&global_screen,
					lives,startlives,
					&score,
					&level_time,
					conf_player.name,
					current_course->name,
					current_level,
					iSubLev, hitbin);
				return -1;
			}
			
			if (ret == -2)
			{
				// quit to menu?

				/*
				delete inter_modal;
				inter_modal = 0;
				*/

				return -2;
			}
		}

		return -1;
	}
};

struct MENU_MODAL : MODAL
{
	MODAL* sub_modal; // currently null or campaign_modal
	MODAL* hold_modal;

	MENU_MODAL()
	{
		hold_modal = 0;
		sub_modal = 0;
	}

	virtual ~MENU_MODAL()
	{
		if (sub_modal)
			delete sub_modal;
		if (hold_modal)
			delete hold_modal;

		hold_modal = 0;
		sub_modal = 0;
		FreeMenu();
	}

	virtual int Run()
	{
		if (sub_modal)
		{
			// campaign / ... ?
			int ret = sub_modal->Run();

			if (ret == -3)
			{
				// quit to menu doesn't change current play pattern
				// ensures only we have full range loop
				PlayLoop(0,-1,0,29);				

				// permanent end
				if (hold_modal)
				{
					delete hold_modal;
					hold_modal = 0;
				}
				if (sub_modal)
				{
					delete sub_modal;
					sub_modal = 0;
				}

				return 0;
			}
			else
			if (ret == -2)
			{
				// temporal exit to menu
				// doesn't change mo3 loop at all !

				if (hold_modal)
				{
					delete hold_modal;
					hold_modal = 0;
				}

				hold_modal = sub_modal;
				sub_modal = 0;

				/*
				delete sub_modal;
				sub_modal = 0;
				*/

				return 0;
			}


			/*
			if (ret<0)
			{
				// back to menu (game over or user requested exit)
				sub_modal=0;
			}
			*/
		}
		else
		{
			int ret = RunMenu(&global_screen);

			if (ret == -3)
			{
				if (sub_modal)
					delete sub_modal;

				sub_modal = 0;

				if (hold_modal)
				{
					// same mo3 loop !
					// (not modified when temporarily entering menu during game)

					sub_modal = hold_modal;
					hold_modal = 0;
				}
				else
				{
					// new game will enter new loop after fade
					FadeOut(2000);

					sub_modal = new CAMPAIGN_MODAL(&global_screen,
						conf_campaign.course,
						conf_campaign.level >= 0 ? conf_campaign.level : 0);
				}
			}
		}

		return 0;
	}
};

